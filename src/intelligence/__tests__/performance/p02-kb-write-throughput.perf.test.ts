/**
 * Phase 826 / C13 — P2: KB write throughput
 *
 * Write 1000 findings to KBStore in < 2000ms.
 * Advisory — warn on CI flakiness.
 *
 * Phase 826 / D-26-40.
 */

import { describe, it, expect } from 'vitest';
import { createTestKB } from '../_harness/kb-factory.js';
import type { Finding } from '../../types.js';

describe('P2/KB: write 1000 findings under 2000ms (PERF — WARN ONLY)', () => {
  it('writeFindings × 1000 completes within 2000ms', async () => {
    const kbHandle = createTestKB();
    try {
      const kb = await kbHandle.createKBStore();

      const snapshotId = 'S-perf-write-001';
      kbHandle.db.prepare(
        "INSERT OR IGNORE INTO snapshots (id, project_id, taken_at, git_sha, files_scanned, loc_total) VALUES (?,?,?,NULL,1000,100000)"
      ).run(snapshotId, 'test-proj', new Date().toISOString());

      const findings: Finding[] = Array.from({ length: 1000 }, (_, i) => ({
        id: `F-perf-${i.toString().padStart(4, '0')}` as `F-${string}`,
        project_id: 'test-proj',
        kind: (i % 2 === 0 ? 'hot_spot' : 'dead_code') as Finding['kind'],
        severity: 'med',
        confidence: 0.7,
        title: `Perf finding ${i}`,
        rationale: `Rationale for finding ${i} — auto-generated by performance test`,
        produced_by: 'analyzer',
        produced_at: new Date().toISOString(),
        snapshot_id: snapshotId,
        status: 'open',
      }));

      const start = performance.now();
      await kb.writeFindings(snapshotId, 'test-proj', findings);
      const elapsed = performance.now() - start;

      // PERF — WARN ONLY: log but don't hard-fail on CI
      if (elapsed > 2000) {
        console.warn(`P2 PERF WARN: writeFindings × 1000 took ${elapsed.toFixed(0)}ms (target: <2000ms)`);
      }
      expect(elapsed).toBeLessThan(5000); // Hard limit at 5x target

      const all = await kb.listOpenFindings('test-proj');
      expect(all.length).toBe(1000);
    } finally {
      kbHandle.cleanup();
    }
  });
});
