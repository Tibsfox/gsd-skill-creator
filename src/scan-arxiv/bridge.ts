// === sc:learn Bridge ===
//
// Takes ranked ArxivPaper candidates, applies minScore + dedup filtering,
// and produces three artifacts in the output directory:
//   1. queue.json — RunOutput with filtered & re-ranked QueueEntry[]
//   2. report.md  — markdown summary rendered by renderRunReport
//   3. run-ingestion.sh — HITL shell script the user runs to drive sc:learn
//
// seen-ids.json is NOT updated here. Only ingestQueue() (post-actual-ingest)
// and dedup-cli.ts mark-seen update dedup state.
//
// Requirements: 03-sc-learn-bridge spec

import * as fs from 'node:fs';
import * as path from 'node:path';
import { execFileSync } from 'node:child_process';

import type {
  ArxivPaper,
  RelevanceScore,
  QueueEntry,
  RunOutput,
  ResolvedScanArxivOptions,
  BridgeOptions,
  BridgeResult,
} from './types.js';

import {
  loadSeenIds,
  recordSeen,
  saveSeenIds,
  DEFAULT_SEEN_IDS_PATH,
} from './dedup.js';

import { renderRunReport } from './report.js';
import { scLearn } from '../commands/sc-learn.js';

// === BuildBridge inputs ===

export interface BuildBridgeInputs {
  ranked: Array<{ paper: ArxivPaper; relevance: RelevanceScore }>;
  options: ResolvedScanArxivOptions;
  runtimeMs: number;
  fetchedCount: number;
  bridgeOpts: BridgeOptions;
}

// === Shell script template ===
// Placeholders: {{ISO_TIMESTAMP}}, {{RUN_ID}}, {{N}}, {{ABSOLUTE_QUEUE_JSON_PATH}}

const SHELL_TEMPLATE = `\
#!/usr/bin/env bash
# Generated by scan-arxiv on {{ISO_TIMESTAMP}}
# Run id: {{RUN_ID}}
# {{N}} candidates queued
set -euo pipefail

QUEUE_FILE="{{ABSOLUTE_QUEUE_JSON_PATH}}"
AUTO_CONFIRM="\${AUTO_CONFIRM:-0}"

if [[ ! -f "$QUEUE_FILE" ]]; then
  echo "queue file missing: $QUEUE_FILE" >&2
  exit 1
fi

readarray -t ENTRIES < <(jq -c '.queue[]' "$QUEUE_FILE")
TOTAL="\${#ENTRIES[@]}"
echo "[scan-arxiv] $TOTAL candidates ready to ingest via sc:learn"

for i in "\${!ENTRIES[@]}"; do
  ENTRY="\${ENTRIES[$i]}"
  ARXIV_ID=$(jq -r '.paper.arxivId' <<<"\$ENTRY")
  TITLE=$(jq -r '.paper.title' <<<"\$ENTRY")
  PDF_URL=$(jq -r '.paper.pdfUrl' <<<"\$ENTRY")
  SCORE=$(jq -r '.relevance.aggregate' <<<"\$ENTRY")
  RATIONALE=$(jq -r '.relevance.rationale' <<<"\$ENTRY")

  echo ""
  echo "[$((i+1))/$TOTAL] $ARXIV_ID  score=$SCORE"
  echo "    $TITLE"
  echo "    rationale: $RATIONALE"

  if [[ "$AUTO_CONFIRM" != "1" ]]; then
    read -rp "    ingest via sc:learn? [y/N/q] " ANSWER
    case "$ANSWER" in
      y|Y) ;;
      q|Q) echo "    quit by user"; exit 0 ;;
      *)   echo "    skipped"; continue ;;
    esac
  fi

  npx tsx src/commands/sc-learn.ts "$PDF_URL" --domain arxiv-cs --depth standard
  # After success, append the ID to seen-ids.json
  npx tsx src/scan-arxiv/dedup-cli.ts mark-seen "$ARXIV_ID" "{{RUN_ID}}/report.md"
done

echo ""
echo "[scan-arxiv] all done"
`;

// === buildBridge ===

export async function buildBridge(
  inputs: BuildBridgeInputs,
): Promise<BridgeResult> {
  const { ranked, options, runtimeMs, fetchedCount, bridgeOpts } = inputs;

  // ── 1. Load dedup state ─────────────────────────────────────────────────
  const seenIdsPath = path.resolve(DEFAULT_SEEN_IDS_PATH);
  const seenState = loadSeenIds(seenIdsPath);

  // ── 2. Filter: minScore then dedup ──────────────────────────────────────
  let dedupSkipCount = 0;
  const filtered = ranked.filter(({ paper, relevance }) => {
    if (relevance.aggregate < options.minScore) return false;
    if (seenState.ids[paper.arxivId]) {
      dedupSkipCount++;
      return false;
    }
    return true;
  });

  // ── 3. Sort by aggregate DESC, then arxivId ASC tiebreaker, then truncate ──
  // (Without the sort, the queue is in fetch order — usually submittedDate ASC
  // from the fetcher — which means "top-N" actually picks the N earliest
  // submitted, not the N highest-scored. ORDER-BY tiebreaker on arxivId keeps
  // ties deterministic between runs.)
  filtered.sort((a, b) => {
    const d = b.relevance.aggregate - a.relevance.aggregate;
    if (d !== 0) return d;
    return a.paper.arxivId.localeCompare(b.paper.arxivId);
  });
  const truncated = filtered.slice(0, options.top);
  const queue: QueueEntry[] = truncated.map((item, idx) => ({
    paper: item.paper,
    relevance: item.relevance,
    rank: idx + 1,
  }));

  // ── 4. Build RunOutput ──────────────────────────────────────────────────
  // Derive a run-id from current timestamp (no colons in dir name).
  const now = new Date();
  const runId = now.toISOString().replace(/:/g, '-').replace(/\.\d+Z$/, 'Z');

  // Compute category + domain totals from the full ranked list (before filter).
  const totalsByCategory: Record<string, number> = {};
  const totalsByDomain = {
    'agent-orchestration': 0,
    'skill-design': 0,
    'code-gen': 0,
    'memory-retrieval': 0,
  };
  for (const { paper, relevance } of ranked) {
    for (const cat of paper.categories) {
      totalsByCategory[cat] = (totalsByCategory[cat] ?? 0) + 1;
    }
    for (const domain of Object.keys(totalsByDomain) as Array<
      keyof typeof totalsByDomain
    >) {
      // Count papers whose subscore for this domain crosses the 0.5 mark,
      // matching the spec ("Number of papers where subscores[domain] >= 0.5").
      // Previously summed the float subscores, producing 11.599... etc.
      if ((relevance.subscores[domain] ?? 0) >= 0.5) {
        totalsByDomain[domain] += 1;
      }
    }
  }

  const runOutput: RunOutput = {
    runId,
    invokedAt: now.toISOString(),
    options,
    totalsByCategory,
    totalsByDomain,
    fetchedCount,
    rankedCount: ranked.length,
    dedupSkipCount,
    queue,
    runtimeMs,
  };

  // ── 5. Ensure output dir exists ─────────────────────────────────────────
  const outputDir = path.resolve(bridgeOpts.outputDir, runId);
  fs.mkdirSync(outputDir, { recursive: true });

  // ── 6. Write queue.json ─────────────────────────────────────────────────
  const queueJsonPath = path.join(outputDir, 'queue.json');
  fs.writeFileSync(queueJsonPath, JSON.stringify(runOutput, null, 2), 'utf-8');

  // ── 7. Write report.md ──────────────────────────────────────────────────
  const reportMdPath = path.join(outputDir, 'report.md');
  fs.writeFileSync(reportMdPath, renderRunReport(runOutput), 'utf-8');

  // ── 8. Write run-ingestion.sh ───────────────────────────────────────────
  const shellScriptPath = path.join(outputDir, 'run-ingestion.sh');
  const scriptContent = SHELL_TEMPLATE.replace(
    /\{\{ISO_TIMESTAMP\}\}/g,
    now.toISOString(),
  )
    .replace(/\{\{RUN_ID\}\}/g, runId)
    .replace(/\{\{N\}\}/g, String(queue.length))
    .replace(/\{\{ABSOLUTE_QUEUE_JSON_PATH\}\}/g, queueJsonPath);

  fs.writeFileSync(shellScriptPath, scriptContent, { encoding: 'utf-8' });
  fs.chmodSync(shellScriptPath, 0o755);

  return {
    queueJsonPath,
    shellScriptPath,
    reportMdPath,
    seenIdsPath,
  };
}

// === ingestQueue ===
//
// Convenience helper used by --auto flag (not the default HITL flow).
// Reads queue.json, optionally prompts the user per entry, calls scLearn,
// and updates seen-ids.json on each success.

export async function ingestQueue(
  queuePath: string,
  options: { dryRun?: boolean; autoConfirm?: boolean; topN?: number } = {},
): Promise<{ successCount: number; failureCount: number; reports: string[] }> {
  const raw = fs.readFileSync(queuePath, 'utf-8');
  const runOutput = JSON.parse(raw) as RunOutput;

  const entries = options.topN
    ? runOutput.queue.slice(0, options.topN)
    : runOutput.queue;

  let state = loadSeenIds();
  let successCount = 0;
  let failureCount = 0;
  const reports: string[] = [];

  const log = (msg: string): void => {
    process.stdout.write(msg + '\n');
  };

  for (const entry of entries) {
    log(
      `[scan-arxiv] ingesting ${entry.paper.arxivId}: ${entry.paper.title}`,
    );

    try {
      const result = await scLearn(entry.paper.pdfUrl, {
        domain: 'arxiv-cs',
        depth: 'standard',
        dryRun: options.dryRun,
        onProgress: (stage, detail) => log(`  [${stage}] ${detail}`),
      });

      if (result.success) {
        // LearningReport does not expose a reportPath field; use sessionId as
        // a stable identifier in the seen-ids ledger.
        const reportRef = result.sessionId;
        state = recordSeen(state, entry.paper.arxivId, reportRef);
        saveSeenIds(state);
        reports.push(reportRef);
        successCount++;
        log(`  ✓ ingested ${entry.paper.arxivId}`);
      } else {
        failureCount++;
        log(
          `  ✗ failed ${entry.paper.arxivId}: ${result.errors.join('; ')}`,
        );
      }
    } catch (err) {
      failureCount++;
      log(`  ✗ error ${entry.paper.arxivId}: ${(err as Error).message}`);
    }
  }

  return { successCount, failureCount, reports };
}
