/**
 * Netscape-format cookie jar for the curl module.
 *
 * Loads and saves cookies in the standard Netscape cookie file format,
 * matches cookies to URLs by domain/path/secure/expiry, and provides
 * a Cookie header builder for request injection.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import type { CurlCookie } from './types.js';

const NETSCAPE_HEADER = '# Netscape HTTP Cookie File\n# https://curl.se/docs/http-cookies.html\n# This file was generated by gsd-skill-creator. Edit at your own risk.\n\n';

/**
 * Parse a single line of a Netscape cookie file into a CurlCookie.
 * Returns undefined for comments, blank lines, or malformed entries.
 */
function parseLine(line: string): CurlCookie | undefined {
  const trimmed = line.trim();
  if (trimmed === '' || (trimmed.startsWith('#') && !trimmed.startsWith('#HttpOnly_'))) {
    return undefined;
  }

  let httpOnly = false;
  let workLine = trimmed;

  if (workLine.startsWith('#HttpOnly_')) {
    httpOnly = true;
    workLine = workLine.slice('#HttpOnly_'.length);
  }

  const fields = workLine.split('\t');
  if (fields.length < 7) return undefined;

  return {
    domain: fields[0],
    includeSubdomains: fields[1].toUpperCase() === 'TRUE',
    path: fields[2],
    secure: fields[3].toUpperCase() === 'TRUE',
    expiry: parseInt(fields[4], 10) || 0,
    name: fields[5],
    value: fields[6],
    httpOnly,
  };
}

/**
 * Format a CurlCookie as a Netscape cookie file line.
 */
function formatLine(cookie: CurlCookie): string {
  const prefix = cookie.httpOnly ? '#HttpOnly_' : '';
  return `${prefix}${cookie.domain}\t${cookie.includeSubdomains ? 'TRUE' : 'FALSE'}\t${cookie.path}\t${cookie.secure ? 'TRUE' : 'FALSE'}\t${cookie.expiry}\t${cookie.name}\t${cookie.value}`;
}

/**
 * Persistent cookie jar with Netscape format load/save and URL-based matching.
 */
export class CurlCookieJar {
  private cookies: CurlCookie[] = [];

  /**
   * Load cookies from a Netscape-format cookie file.
   */
  load(filePath: string): void {
    const content = readFileSync(filePath, 'utf-8');
    const lines = content.split('\n');
    this.cookies = [];
    for (const line of lines) {
      const cookie = parseLine(line);
      if (cookie) this.cookies.push(cookie);
    }
  }

  /**
   * Save cookies to a Netscape-format cookie file.
   */
  save(filePath: string): void {
    const lines = this.cookies.map(formatLine);
    writeFileSync(filePath, NETSCAPE_HEADER + lines.join('\n') + '\n', 'utf-8');
  }

  /**
   * Get cookies that match a given URL by domain, path, secure, and expiry.
   */
  getCookiesForUrl(url: string): CurlCookie[] {
    const parsed = new URL(url);
    const hostname = parsed.hostname;
    const path = parsed.pathname;
    const isSecure = parsed.protocol === 'https:';
    const now = Math.floor(Date.now() / 1000);

    return this.cookies.filter((cookie) => {
      // Domain matching
      if (cookie.includeSubdomains) {
        // Must match exactly or be a subdomain (with dot boundary)
        if (hostname !== cookie.domain && !hostname.endsWith('.' + cookie.domain)) {
          return false;
        }
      } else {
        if (hostname !== cookie.domain) return false;
      }

      // Path prefix matching
      if (!path.startsWith(cookie.path)) return false;

      // Secure flag: only send secure cookies over HTTPS
      if (cookie.secure && !isSecure) return false;

      // Expiry check: session cookies (0) never expire
      if (cookie.expiry > 0 && cookie.expiry < now) return false;

      return true;
    });
  }

  /**
   * Build a Cookie header value for the given URL.
   * Returns undefined if no cookies match.
   */
  buildCookieHeader(url: string): string | undefined {
    const matching = this.getCookiesForUrl(url);
    if (matching.length === 0) return undefined;
    return matching.map((c) => `${c.name}=${c.value}`).join('; ');
  }

  /**
   * Add a cookie, replacing any existing cookie with the same domain+path+name.
   */
  addCookie(cookie: CurlCookie): void {
    const idx = this.cookies.findIndex(
      (c) => c.domain === cookie.domain && c.path === cookie.path && c.name === cookie.name,
    );
    if (idx >= 0) {
      this.cookies[idx] = cookie;
    } else {
      this.cookies.push(cookie);
    }
  }

  /**
   * Get all cookies in the jar (read-only).
   */
  getAll(): readonly CurlCookie[] {
    return this.cookies;
  }

  /**
   * Clear all cookies from the jar.
   */
  clear(): void {
    this.cookies = [];
  }
}
