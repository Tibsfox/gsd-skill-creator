#!/usr/bin/env node
// Read the populated local PG (constellation.* schema) and emit a SQL file that
// brings the live MySQL on tibsfox.com up to the same content WITHOUT touching
// existing rows it doesn't own.
//
// Strategy:
//   - INSERT IGNORE for new project ids (artists A000..A063, species B000..B063,
//     legacy NASA N100..N121 + NART, and catalog M<version> ids).
//   - Populate the artists / species / nasa_missions extension tables.
//   - INSERT IGNORE for cross_refs that the local PG knows about but the live
//     cross_refs table does not (artist↔artist, species↔species, mission↔mission).
//
// Output: stdout SQL, suitable for `node emit-mysql-seed.mjs | mysql ...`.

import pg from 'pg';

const client = new pg.Client({
  host: process.env.PG_HOST || 'localhost',
  port: Number(process.env.PG_PORT || 5432),
  user: process.env.PG_USER || 'maple',
  password: process.env.PG_PASSWORD || '',
  database: process.env.PG_DB || 'tibsfox',
});
await client.connect();
await client.query('SET search_path TO constellation, public');

function sqlEscape(v) {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return v ? '1' : '0';
  if (v instanceof Date) return "'" + v.toISOString().slice(0, 10) + "'";
  return "'" + String(v).replace(/\\/g, '\\\\').replace(/'/g, "''") + "'";
}

// --- which project ids belong to which extension wing ----------------------
// (existing MySQL projects table uses cluster names: 'Music', 'Ecology', 'Space')
function clusterFor(id) {
  if (/^A\d{3}$/.test(id)) return 'Music';
  if (/^B\d{3}$/.test(id)) return 'Ecology';
  if (/^N\d{3}$/.test(id) || id === 'NART') return 'Space';
  if (/^M[\d.]+$/.test(id)) return 'Space';
  return null;
}

const out = [];
out.push('-- generated by emit-mysql-seed.mjs at ' + new Date().toISOString());
out.push('SET NAMES utf8mb4;');
out.push('SET FOREIGN_KEY_CHECKS = 1;');
out.push('START TRANSACTION;');
out.push('');

// 1. project rows that should exist in MySQL but probably don't
const ext = await client.query(`
  SELECT p.id, p.name
    FROM projects p
   WHERE p.id ~ '^(A[0-9]{3}|B[0-9]{3}|N[0-9]{3}|NART|M[0-9.]+)$'
   ORDER BY p.id
`);
out.push('-- extension project rows (artists / species / NASA legacy + catalog)');
for (const r of ext.rows) {
  const c = clusterFor(r.id);
  // only fill the columns we know — let MySQL defaults handle the rest
  // existing projects schema: id, name, path, cluster, cluster_color, modules,
  //                           total_lines, version_added, title, subtitle, through_line
  // path/title default to '' NOT NULL; we have to fill them.
  out.push(
    `INSERT IGNORE INTO projects (id, name, path, cluster, cluster_color, version_added, title) VALUES (` +
    `${sqlEscape(r.id)}, ${sqlEscape(r.name)}, '', ${sqlEscape(c)}, '#999999', '', ${sqlEscape(r.name)});`
  );
}
out.push('');

// 2. artists extension rows
const artists = await client.query(
  'SELECT project_id, artist, genre, energy, xrefs FROM artists ORDER BY project_id'
);
out.push('-- artists extension table (' + artists.rows.length + ' rows)');
for (const a of artists.rows) {
  const xrefsJson = JSON.stringify(a.xrefs || []);
  out.push(
    `INSERT INTO artists (project_id, artist, genre, energy, xrefs) VALUES (` +
    `${sqlEscape(a.project_id)}, ${sqlEscape(a.artist)}, ${sqlEscape(a.genre)}, ${sqlEscape(a.energy)}, ${sqlEscape(xrefsJson)})` +
    ` ON DUPLICATE KEY UPDATE artist=VALUES(artist), genre=VALUES(genre), energy=VALUES(energy), xrefs=VALUES(xrefs);`
  );
}
out.push('');

// 3. species
const species = await client.query(
  'SELECT project_id, common, scientific, family, taxon_order, energy, trait FROM species ORDER BY project_id'
);
out.push('-- species extension table (' + species.rows.length + ' rows)');
for (const s of species.rows) {
  out.push(
    `INSERT INTO species (project_id, common, scientific, family, taxon_order, energy, trait) VALUES (` +
    `${sqlEscape(s.project_id)}, ${sqlEscape(s.common)}, ${sqlEscape(s.scientific)}, ${sqlEscape(s.family)}, ${sqlEscape(s.taxon_order)}, ${sqlEscape(s.energy)}, ${sqlEscape(s.trait)})` +
    ` ON DUPLICATE KEY UPDATE common=VALUES(common), scientific=VALUES(scientific), family=VALUES(family), taxon_order=VALUES(taxon_order), energy=VALUES(energy), trait=VALUES(trait);`
  );
}
out.push('');

// 4. NASA missions
const nasa = await client.query(
  'SELECT project_id, title, launch_date, organism, sci_name, node_count, crew, is_artemis, catalog_id FROM nasa_missions ORDER BY project_id'
);
out.push('-- nasa_missions extension table (' + nasa.rows.length + ' rows)');
for (const n of nasa.rows) {
  out.push(
    `INSERT INTO nasa_missions (project_id, title, launch_date, organism, sci_name, node_count, crew, is_artemis, catalog_id) VALUES (` +
    `${sqlEscape(n.project_id)}, ${sqlEscape(n.title)}, ${sqlEscape(n.launch_date)}, ${sqlEscape(n.organism)}, ${sqlEscape(n.sci_name)}, ${sqlEscape(n.node_count)}, ${sqlEscape(n.crew)}, ${sqlEscape(n.is_artemis)}, ${sqlEscape(n.catalog_id)})` +
    ` ON DUPLICATE KEY UPDATE title=VALUES(title), launch_date=VALUES(launch_date), organism=VALUES(organism), sci_name=VALUES(sci_name), node_count=VALUES(node_count), crew=VALUES(crew), is_artemis=VALUES(is_artemis), catalog_id=VALUES(catalog_id);`
  );
}
out.push('');

// 5. cross_refs — only edges that involve the extension namespaces.
// Existing MySQL cross_refs already has the project↔project chunk; don't double-insert.
const edges = await client.query(`
  SELECT src_id, dst_id FROM edges
   WHERE src_id ~ '^(A[0-9]{3}|B[0-9]{3}|N[0-9]{3}|NART|M[0-9.]+|S36|SPS|MST)$'
      OR dst_id ~ '^(A[0-9]{3}|B[0-9]{3}|N[0-9]{3}|NART|M[0-9.]+|S36|SPS|MST)$'
  ORDER BY src_id, dst_id
`);
out.push('-- extension cross_refs (artist/species/NASA — ' + edges.rows.length + ' rows)');
out.push("-- ref_type defaults to 'cites'; non-extension edges already in cross_refs are untouched.");
for (const e of edges.rows) {
  out.push(
    `INSERT IGNORE INTO cross_refs (source_id, target_id, ref_type) VALUES (` +
    `${sqlEscape(e.src_id)}, ${sqlEscape(e.dst_id)}, 'cites');`
  );
}
out.push('');

// 6. build_log
out.push('-- audit row');
out.push(
  `INSERT INTO build_log (commit_sha, node_count, edge_count, notes) VALUES (` +
  `${sqlEscape(process.env.COMMIT_SHA || '')}, ${ext.rows.length}, ${edges.rows.length}, 'seed via emit-mysql-seed.mjs');`
);
out.push('');
out.push('COMMIT;');

process.stdout.write(out.join('\n') + '\n');
console.error(`emitted ${out.length} lines: ${ext.rows.length} project rows + ${artists.rows.length} artists + ${species.rows.length} species + ${nasa.rows.length} nasa + ${edges.rows.length} edges`);

await client.end();
